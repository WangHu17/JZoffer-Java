public class Solution {
    public ListNode ReverseList(ListNode head) {
        if(head == null)return head;
        ListNode p1=null,p2=head,p3=null;
        while(p2 != null){
            p3 = p2.next;
            p2.next = p1;
            p1 = p2;
            p2 = p3;
        }
        return p1;
    }
}